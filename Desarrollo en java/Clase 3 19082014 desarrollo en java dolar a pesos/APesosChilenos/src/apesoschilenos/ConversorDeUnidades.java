    private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertirActionPerformed
      try {dolar = Float.parseFloat(txtValores.getText());
         euro = Float.parseFloat(txtValores.getText());
          uf = Float.parseFloat(txtValores.getText());
      }catch (Exception e){ JOptionPane.showMessageDialog(null, "Ingrese un valor numerico");}
      
     if(rdbutonDolar.isSelected()) {
    lblPesos.setText("$"+(dolar*576.15));
            lblFecha.setText(dt.format(fecha));}

    if (rdbutonEuro.isSelected()){
    lblPesos.setText("$"+(euro*769.84));
    lblFecha.setText(dt.format(fecha));} 
        
    if (rdbutonUF.isSelected()){
    lblPesos.setText("$"+(uf*24084.78));
    lblFecha.setText(dt.format(fecha));}
    }//GEN-LAST:event_btnConvertirActionPerformed

