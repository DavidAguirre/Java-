    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        if(rdbutonDolar.isSelected())
        {JOptionPane.showMessageDialog(null,"Cantidad en dolar: "+ txtValores.getText()+
                                        "\n Valor en pesos: $"+(dolar*576.15));}
        if(rdbutonEuro.isSelected())
        { JOptionPane.showMessageDialog(null,"Cantidad en Euro: "+ txtValores.getText()+
                                         "\n Valor en pesos: $"+(euro*769.84)); }
        if(rdbutonUF.isSelected())
        { JOptionPane.showMessageDialog(null,"Cantidad en UF: "+ txtValores.getText()+
                                            "\n Valor en pesos: $"+ (uf*24084.78)); }
    }//GEN-LAST:event_btnImprimirActionPerformed

